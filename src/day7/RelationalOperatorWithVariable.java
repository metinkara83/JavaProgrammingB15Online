package day7;

public class RelationalOperatorWithVariable {

    public static void main(String[] args) {

        // > >= < <= == != Relational Operators

        // Relational Operator is used to compare 2 operands and return true or false
        // any action or expression that return true or false result can be called boolean expression

        int myNumber = 10;
        System.out.println(myNumber > 5);
        System.out.println(myNumber < -19);
        System.out.println(myNumber >= 5);
        System.out.println(myNumber >= 8);
        System.out.println(myNumber <= 5);
        System.out.println(myNumber >= 8);

        System.out.println(myNumber == 5);
        System.out.println(myNumber != 10);
    }
}

