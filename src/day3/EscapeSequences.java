package day3;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello\tWorld");
        System.out.println("Hello\nWorld");
        System.out.println("Hello\'World");
        System.out.println("Hello\"World");
        System.out.println("Hello\\World");

        System.out.println("I like the book \"Java\"");
        System.out.println("My favorite movie is \'The Italian Job\'.");

        // what is the equivalent of println and print, using one of the above

        System.out.print("Hello World 1\nHello World 2\n\n");

        System.out.print("This is regular print \n");
        System.out.println("This is println");

    }

}
