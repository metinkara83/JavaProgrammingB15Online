package day7;

public class RelationalOperator {

    public static void main(String[] args) {

        // > >= < <= == != Relational Operators

        // Relational Operator is used to compare 2 operands and return true or false
        // any action or expression that return true or false result can be called boolean expression

        System.out.println(7 > 5);
        System.out.println(7 < -19);
        System.out.println(5 >= 5);
        System.out.println(7 >= 8);
        System.out.println(4 <= 5);
        System.out.println(17 >= 8);

        System.out.println(5 == 5);
        System.out.println(6 != 10);
    }
}
